Chốt phiên này, tại sàn giao dịch COMEX (Mỹ), giá vàng giao ngay ồn định ở mức 1.459,52 USD/ounce. Giá vàng giao kỳ hạn cũng gần như không đổi so với phiên trước đó, đứng ở mức 1.464,90 USD/ounce. 

Thị trường hiện chờ đợi cuộc họp chính sách kéo dài hai ngày của Cục Dự trữ liên bang Mỹ (Fed).

Ngân hàng đầu tư Goldman Sachs cho biết nhu cầu đầu tư vàng có thể tăng trong thời gian tới do lo ngại về suy thoái và bất ổn chính trị. Ngân hàng này dự báo giá vàng sẽ đạt mức 1.600 USD/ounce trong 3-12 tháng tới./.
